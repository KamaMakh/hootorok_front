<template>
  <q-layout view="lHh Lpr lff">
    <q-header elevated>
      <q-toolbar>
        <q-btn
          flat
          dense
          round
          @click="leftDrawerOpen = !leftDrawerOpen"
          aria-label="Menu"
        >
          <q-icon name="menu" />
        </q-btn>

        <q-toolbar-title />

        <div class="row items-center">
          <template v-if="loggedIn">
            <router-link
              :to="{ name: 'cabinet' }"
              class="standard-link text-white q-ml-md"
              v-text="$t('cabinet')"
            />
            <router-link
              :to="{ name: 'logout' }"
              class="standard-link text-white q-ml-md"
              v-text="$t('logout')"
            />
          </template>
          <template v-else>
            <router-link
              :to="{ name: 'login' }"
              class="standard-link text-white q-ml-md"
              v-text="$t('login')"
            />
            <router-link
              :to="{ name: 'registration' }"
              class="standard-link text-white q-ml-md"
              v-text="$t('registration')"
            />
          </template>
          <q-select
            v-model="lang"
            :options="langOptions"
            dense
            borderless
            emit-value
            map-options
            options-dense
            :dark="true"
            class="q-ml-md"
          />
        </div>
      </q-toolbar>
    </q-header>

    <q-drawer v-model="leftDrawerOpen" bordered content-class="bg-grey-2">
      <q-list>
        <q-item-label header>Essential Links</q-item-label>
        <q-item clickable tag="a" target="_blank" href="https://quasar.dev">
          <q-item-section avatar>
            <q-icon name="school" />
          </q-item-section>
          <q-item-section>
            <q-item-label>Docs</q-item-label>
            <q-item-label caption>quasar.dev</q-item-label>
          </q-item-section>
        </q-item>
      </q-list>
    </q-drawer>

    <q-page-container>
      <router-view />

      <q-footer elevated class="text-left q-pa-md">
        <div class="row items-center justify-center text-center q-gutter-md">
          <router-link
            :to="{ name: 'contacts'}"
            class="standard-link text-white"
            v-text="$t('contacts')"
          />
          <router-link
            :to="{ name: 'about'}"
            class="standard-link text-white"
            v-text="$t('about')"
          />
          <router-link
            :to="{ name: 'info'}"
            class="standard-link text-white"
            v-text="$t('info')"
          />
        </div>
      </q-footer>
    </q-page-container>
  </q-layout>
</template>

<script>
import { openURL } from 'quasar';
import { mapGetters } from 'vuex';

export default {
  name: 'MainLayout',
  data() {
    return {
      leftDrawerOpen: false,
      langOptions: [{
        label: 'RU',
        value: 'ru',
      }, {
        label: 'EN',
        value: 'en-us',
      }],
    };
  },
  computed: {
    lang: {
      get() {
        return this.$store.state.lang;
      },
      set(lang) {
        this.$i18n.locale = lang;
        import(`quasar/lang/${lang}`).then((l) => {
          this.$q.lang.set(l.default);
        });
        this.$store.commit('setLang', lang);
      },
    },
    ...mapGetters(['loggedIn']),
  },
  methods: {
    openURL,
  },
};
</script>
